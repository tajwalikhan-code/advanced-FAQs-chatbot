# =========================================================
# SEMANTIC SEARCH FUNCTION
# =========================================================

def semantic_search(
    query,
    category_filter="All",
    top_k=5
):
    """
    Semantic FAQ Retrieval using Sentence Transformers + FAISS
    """

    try:

        # -----------------------------
        # Validate Query
        # -----------------------------
        if query is None or not str(query).strip():
            return {
                "answer": "⚠️ Please enter a valid question.",
                "confidence": 0,
                "related": []
            }

        query = str(query).strip()

        # -----------------------------
        # Encode Query
        # -----------------------------
        query_embedding = model.encode(
            [query],
            convert_to_numpy=True
        )

        faiss.normalize_L2(query_embedding)

        # -----------------------------
        # Search FAISS
        # -----------------------------
        scores, indices = index.search(
            query_embedding,
            min(top_k * 3, len(faq_df))
        )

        indices = indices[0]
        scores = scores[0]

        # -----------------------------
        # Build Results
        # -----------------------------
        results = []

        for idx, score in zip(indices, scores):

            # Prevent index errors
            if idx < 0 or idx >= len(faq_df):
                continue

            row = faq_df.iloc[idx]

            category = row["category"]

            # Apply category filter
            if category_filter != "All":
                if category != category_filter:
                    continue

            results.append({
                "question": row["question"],
                "answer": row["answer"],
                "category": category,
                "score": float(score)
            })

        # -----------------------------
        # No Results
        # -----------------------------
        if len(results) == 0:
            return {
                "answer": "❌ No matching FAQ found.",
                "confidence": 0,
                "related": []
            }

        # -----------------------------
        # Best Match
        # -----------------------------
        best = results[0]

        confidence = int(
            max(0, min(best["score"] * 100, 100))
        )

        # -----------------------------
        # Related Questions
        # -----------------------------
        related = []

        for item in results[1:4]:
            related.append(item["question"])

        return {
            "answer": best["answer"],
            "confidence": confidence,
            "related": related
        }

    except Exception as e:

        return {
            "answer": f"⚠️ Error occurred: {str(e)}",
            "confidence": 0,
            "related": []
        }

