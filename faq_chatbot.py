# CHATBOT RESPONSE FUNCTION
# =========================================================

def chatbot_response(message, history, category):

    try:

        if history is None:
            history = []

        result = semantic_search(
            query=message,
            category_filter=category,
            top_k=5
        )

        answer = result["answer"]
        confidence = result["confidence"]
        related = result["related"]

        # -----------------------------
        # Confidence Bar
        # -----------------------------
        bar_length = 20

        filled = int((confidence / 100) * bar_length)

        progress_bar = (
            "🟩" * filled +
            "⬜" * (bar_length - filled)
        )

        response = f"""
### 🤖 Answer

{answer}

---

### 📊 Confidence Score

{confidence}%
{progress_bar}

"""

        # -----------------------------
        # Related Questions
        # -----------------------------
        if related:

            response += "\n### 🔗 Related Questions\n"

            for q in related:
                response += f"- {q}\n"

        history.append((message, response))

        return history, history

    except Exception as e:

        error_response = f"⚠️ System Error: {str(e)}"

        history.append((message, error_response))

        return history, history

