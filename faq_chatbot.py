def browse_faqs(category):

    try:

        data = faq_df.copy()

        if category != "All":
            data = data[data["category"] == category]

        if len(data) == 0:
            return pd.DataFrame({
                "Message": ["No FAQs found"]
            })

        return data[["question", "answer", "category"]]

    except Exception as e:

        return pd.DataFrame({
            "Error": [str(e)]
        })

